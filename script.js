// Affiche une page
function showPage(pageId) {
    document.querySelectorAll('.page').forEach(page => page.classList.remove('active'));
    document.querySelectorAll('.nav-btn').forEach(btn => btn.classList.remove('active'));
    document.getElementById(pageId).classList.add('active');
    
    // Find the button that corresponds to the pageId and activate it
    const targetButton = Array.from(document.querySelectorAll('.nav-btn')).find(btn => {
        const onclickAttr = btn.getAttribute('onclick');
        return onclickAttr && (onclickAttr.includes(`showPage('${pageId}')`) || onclickAttr.includes(`checkAccess('${pageId}')`));
    });

    if (targetButton) {
        targetButton.classList.add('active');
    }
}


